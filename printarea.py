#Describe what the program will do -- The following code will calculate the following Areas of a Rectangle and Triangle.

#The following code will calculate the Area of a Rectangle
print("Area of a Rectangle")
recWidth = 3
recHeight = 5
recArea = recWidth * recHeight
print("The area of a rectangle is " + str(recArea) + ".")

#The following code will calculate the Area of a Triangle.
print("Area of a Triangle")
triBase = 3
triHeight = 4
triArea = (triBase * triHeight) * 0.5
print("The area of a triangle is " + str(triArea) + ".")